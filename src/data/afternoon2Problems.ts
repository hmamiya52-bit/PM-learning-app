import type { Afternoon2Problem } from '../types'

const RL = (y: number) => `R${y}（${y + 2018}年）`
const R = (y: number) => `R${y}`

const IPA_PROBLEM_LIST = 'https://www.ipa.go.jp/shiken/mondai-kaiotu/index.html'

function p(
  year: string,
  yearLabel: string,
  number: 1 | 2,
  title: string,
  theme: string,
  keywords: string[],
  promptA: string,
  promptB: string,
  promptC: string,
  questionPdfUrl: string = IPA_PROBLEM_LIST
): Afternoon2Problem {
  return {
    id: `${year}-PM2-${number}`,
    year,
    yearLabel,
    number,
    title,
    theme,
    keywords,
    promptA,
    promptB,
    promptC,
    questionPdfUrl,
  }
}

