const nowIso = '2025-01-01T00:00:00.000Z'

function mk(
  id: string,
  category: Note['category'],
  title: string,
  content: string,
  tags: string[] = []
): Note {
  return {
    id: `seed:${id}`,
    category,
    title,
    content: content.trim(),
    tags,
    seed: true,
    createdAt: nowIso,
    updatedAt: nowIso,
  }
}

